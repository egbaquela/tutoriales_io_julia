md"Además, hay que tener en cuenta las siguientes restricciones: los empleados no pueden quedarse sin tarea, al empleado E2 sólo se le puede asignar una tarea, y las tareas no se pueden compartir."

# ╔═╡ 603154d4-b8e8-40d9-9ae5-a1d8321a37bc
md"### Formulación"

# ╔═╡ 22e6886f-aa89-4baa-9dce-2a999f475239
md"
Este problema es un caso particular del problema de transporte en el que la demanda de cada destino (tareas) es 1 y cada origen (empleado) tiene una oferta suficiente para hacer todas las tareas (5), excepto por el empleado E2 que solo puede hacer una tarea por lo que su 'oferta' será 1.

Para formular el problema, necesitamos las variables:

* _ $X_{i,j} \in \{0,1\}$ : variable binaria que indica si el empleado $i$ hace la tarea $j$ o no. 

Con este esquema de variables, podemos armar una función objetivo que maximice la valoración total (suma de valoraciones individuales) de los empleados que asignemos a las tareas:

$Max \ Z=\sum_{i \in I} \sum_{j \in J}X_{i,j} \cdot V_{i,j}$

Además, se deben cumplir las restricciones de capacidad de oferta (cada empleado puede hacer, como mucho, todas las tareas, excepto por E2 donde $O_{E2}=1$):

$\sum_{j \in J}X_{i,j} \leq O_{i}, \forall i \in I$

Las restricciones de capacidad de demanda (cada tarea requiere un empleado, no puede compartir):

$\sum_{i \in I}X_{i,j} = 1, \forall j \in J$

Además, un empleado no puede quedarse sin tareas (mínimo hace una tarea):

$\sum_{j \in J}X_{i,j} \geq 1, \forall i \in I$

Es imposible que el empleado E2 haga la tarea T3 o la T5:
$X_{E2,T3} = 0$
$X_{E2,T5} = 0$

La formulación completa queda como:

$Max \ Z=\sum_{i \in I} \sum_{j \in J}X_{i,j} \cdot V_{i,j}$

$\sum_{j \in J}X_{i,j} \leq O_{i}, \forall i \in I$
$\sum_{i \in I}X_{i,j} = 1, \forall j \in J$
$\sum_{j \in J}X_{i,j} \geq 1, \forall i \in I$
$X_{E2,T3} = 0$
$X_{E2,T5} = 0$

$X_{i,j} \in \{0,1 \}$ 

"

