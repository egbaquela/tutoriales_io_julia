md"En el problema de asignación se tiene a un conjunto de recursos y a un conjunto de actividades. En su versión más simple, el objetivo es asignarle a cada único recurso una única actividad y no hay una diferencia de costo para cada combinación. Al complejizar el problema se pueden agregar costos diferenciados para cada par de recurso y actividad, restricciones de disponibilidad del recurso para hacer determinadas actividades, etcétera. Por ejemplo, el problema de asignación se puede utilizar para definir los turnos que cada trabajador realizará en una empresa."

