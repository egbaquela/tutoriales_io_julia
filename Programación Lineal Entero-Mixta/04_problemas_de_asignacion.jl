begin
	model_01 = Model(HiGHS.Optimizer) #

	# Dimensiones
	empleados_01 = []
	num_empleados_01 = 4
	for i in 1:num_empleados_01
		push!(empleados_01, string("E",i)) 
	end
	tareas_01 = []
	num_tareas_01 = 5
	for i in 1:num_tareas_01
		push!(tareas_01, string("T",i)) 
	end
	
	# Datos
	valoracion_01 = NamedArray([[6 8 9 3 7]
							    [2 3 0 4 0]
							    [5 6 8 9 6]
								[2 3 7 8 6]],(empleados_01,tareas_01))
	
	
	oferta_01 = NamedArray([5, 1, 5, 5], empleados_01)
	
	demanda_01 = NamedArray([1, 1, 1, 1, 1], tareas_01)

	# Declaro las variables de decisión.
	x_01 = @variable(model_01, x_01[empleados_01,tareas_01], Bin)
	
	#Función objetivo
	obj_01 = @objective(model_01, Max,
		sum([sum([valoracion_01[i,j]*x_01[i,j] for i in empleados_01]) for j in tareas_01]))

	# Restriccion de capacidad en oferta.
	r_01 = @constraint(model_01, [i in empleados_01], sum([x_01[i,j] for j in tareas_01])  <= oferta_01[i])
	
	# Restriccion de capacidad en demanda (no comparte).
	r2_01  = @constraint(model_01, [j in tareas_01], sum([x_01[i,j] for i in empleados_01])  == demanda_01[j])

	# Restricción de al menos una tarea por empleado.
	r3_01  = @constraint(model_01, [i in empleados_01], sum([x_01[i,j] for j in tareas_01])  >= 1)

	# Restricción E2 no puede hacer T3.
	r4_01  = @constraint(model_01, x_01["E2","T3"] == 0)
	
	# Restricción E2 no puede hacer T5.
	r5_01  = @constraint(model_01, x_01["E2","T5"] == 0)	
		
	latex_formulation(model_01)
end

