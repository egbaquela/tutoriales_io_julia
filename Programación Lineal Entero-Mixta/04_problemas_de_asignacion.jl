# ╔═╡ cb048a43-d1fa-4fa2-8198-446f5bca6a9a
md"Le llamábamos $X_{i,j}$ a la cantidad que decidíamos transportar desde el origen $i$ al destino $j$. Y queríamos minimizar:

$Min Z = \sum_{i \in I} \sum_{j \in J}CT_{i,j} = \sum_{i \in I} \sum_{j \in J}C_{i,j} \cdot X_{i,j}$

En este problema, se necesitaban restricciones que aseguren que la suma de lo enviado desde un origen $i$ fuera menor o igual a la oferta de ese origen $O_{i}$ y que la suma de lo enviado a un destino $j$ fuera igual a la demanda de ese destino $D_{j}$

$\sum_{j \in J}X_{i,j} \leq O_{i}, \forall i \in I$
$\sum_{i \in I}X_{i,j} = D_{j}, \forall j \in J$

Además de la no negatividad:

$X_{i,j} \geq 0, \forall i \in I, j \in J$
"

